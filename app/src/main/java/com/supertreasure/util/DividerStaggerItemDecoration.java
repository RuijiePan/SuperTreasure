/**
 * Created by Li on 2016/1/16.
 */
public class DividerStaggerItemDecoration extends RecyclerView.ItemDecoration{

    private int space;

    public DividerStaggerItemDecoration(int space) {
        this.space = space;
    }

    @Override
    public void getItemOffsets(Rect outRect, View view, RecyclerView parent, RecyclerView.State state) {

        if(parent.getChildPosition(view) != 0)
            outRect.top = space;
            outRect.right = space;
            outRect.left = 0;
            outRect.bottom =0;
    }
}
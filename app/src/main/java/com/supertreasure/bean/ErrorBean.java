public class ErrorBean {
    private String msg;
    private String status;
    private String error_input;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getError_input() {
        return error_input;
    }

    public void setError_input(String error_input) {
        this.error_input = error_input;
    }
}
